package com.sixty.entity;

import com.sixty.annotation.JsonEntity;
import com.sixty.annotation.JsonId;

@JsonEntity(file = "users.json")
public class User {
    @JsonId
    public Long id;
    public String name;
    public int age;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
